class Solution {
    
public:
    string clearDigits(string s) {
        while (true) {
            bool f = false;
            for (int i = 0; i < s.size(); i++) {
                if (isdigit(s[i])) {
                    for (int j = i - 1; j >= 0; --j) {
                        if (!isdigit(s[j])) {
                       s.erase(j, 1); 
                s.erase(i - 1, 1);
                            f = true;
                            break;
                        }
                    }
                    break;  
                }
            }
            if (!f) 
                break;  
        }
         return s;
    }
};